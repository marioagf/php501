<?php

class Calculadora
{
    public function soma($n1, $n2)
    {
        return $n1 + $n2;
    }
    public function subt($n1, $n2)
    {
        return $n1 - $n2;
    }
    public function mult($n1, $n2)
    {
        return $n1 * $n2;
    }
    public function div($n1, $n2)
    {
        return $n1 / $n2;
    }
}
